package com.mkenlo.activefit.db.model;

public class Goals {

    double weight;
    int steps;

    public Goals() {
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public int getSteps() {
        return steps;
    }

    public void setSteps(int steps) {
        this.steps = steps;
    }
}
